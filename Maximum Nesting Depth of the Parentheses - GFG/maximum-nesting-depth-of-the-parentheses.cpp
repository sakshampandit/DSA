class Solution {
  public:
    int maxDepth(string s) {
        // code here
        int bracket=0,ans=0;
        for(auto x:s)
        {
            if(x=='(')
            bracket++;
            if(x==')')
            bracket--;
            ans=max(ans,bracket);
        }
        return ans;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        string s;
        cin >> s;

        Solution obj;
        int ans = obj.maxDepth(s);
        cout << ans << endl;
    }
    return 0;
}
